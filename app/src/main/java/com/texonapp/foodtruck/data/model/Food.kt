    @SerializedName("id")
    val id:Int? = null

    @SerializedName("name")
    val name:String? = null

    @SerializedName("image")
    val image:String? = null

    @SerializedName("price")
    val price:String? = null

    @SerializedName("description")
    val description:String? = null
}